def rot_word(word):
    return ((word << 8) & 0xFFFFFFFF) | (word >> 24)

def sub_word(word):
    b0 = SBOX[(word >> 24) & 0xFF]
    b1 = SBOX[(word >> 16) & 0xFF]
    b2 = SBOX[(word >> 8) & 0xFF]
    b3 = SBOX[(word) & 0xFF]
    return (b0 << 24) | (b1 << 16) | (b2 << 8) | b3

def inv_key_schedule_round(key_in, rcon):

    w0_in = (key_in >> 96) & 0xFFFFFFFF
    w1_in = (key_in >> 64) & 0xFFFFFFFF
    w2_in = (key_in >> 32) & 0xFFFFFFFF
    w3_in = key_in & 0xFFFFFFFF

    w3_out = w3_in ^ w2_in
    w2_out = w2_in ^ w1_in
    w1_out = w1_in ^ w0_in
    t = sub_word(rot_word(w3_out))

    w0_out = w0_in ^ t ^ (rcon << 24)

    # Combine back to 128-bit
    key_out = (w0_out << 96) | (w1_out << 64) | (w2_out << 32) | w3_out
    return key_out

def generate_all_keys(key10_int):
    keys = {}
    keys[10] = key10_int

    # Rcon values used in KeySaving.v: 36, 1B, 80, 40, 20, 10, 08, 04, 02, 01
    rcons = [0x36, 0x1B, 0x80, 0x40, 0x20, 0x10, 0x08, 0x04, 0x02, 0x01]

    current_key = key10_int
    for i, rcon in enumerate(rcons):
        round_idx = 9 - i
        current_key = inv_key_schedule_round(current_key, rcon)
        keys[round_idx] = current_key

    return keys

def num_to_state(num):
    #Convert 128-bit integer to 4x4 state matrix (Column Major for AES).
    state = [[0]*4 for _ in range(4)]
    for c in range(4):
        for r in range(4):
            shift = 120 - (c * 32 + r * 8)
            state[r][c] = (num >> shift) & 0xFF
    return state

def state_to_num(state):
    num = 0
    for c in range(4):
        for r in range(4):
            num = (num << 8) | state[r][c]
    return num

def inv_sub_bytes(state):
    for r in range(4):
        for c in range(4):
            state[r][c] = INV_SBOX[state[r][c]]
    return state

def inv_shift_rows(state):
    state[1] = state[1][-1:] + state[1][:-1]
    state[2] = state[2][-2:] + state[2][:-2]
    state[3] = state[3][-3:] + state[3][:-3]
    return state

def gmult(a, b):
    p = 0
    for i in range(8):
        if b & 1:
            p ^= a
        hi_bit_set = a & 0x80
        a = (a << 1) & 0xFF
        if hi_bit_set:
            a ^= 0x1B # x^8 + x^4 + x^3 + x + 1
        b >>= 1
    return p

def inv_mix_columns(state):
    for c in range(4):
        col = [state[r][c] for r in range(4)]
        # Matrix multiply in GF(2^8)
        # 0e 0b 0d 09
        # 09 0e 0b 0d
        # 0d 09 0e 0b
        # 0b 0d 09 0e
        state[0][c] = gmult(0x0e, col[0]) ^ gmult(0x0b, col[1]) ^ gmult(0x0d, col[2]) ^ gmult(0x09, col[3])
        state[1][c] = gmult(0x09, col[0]) ^ gmult(0x0e, col[1]) ^ gmult(0x0b, col[2]) ^ gmult(0x0d, col[3])
        state[2][c] = gmult(0x0d, col[0]) ^ gmult(0x09, col[1]) ^ gmult(0x0e, col[2]) ^ gmult(0x0b, col[3])
        state[3][c] = gmult(0x0b, col[0]) ^ gmult(0x0d, col[1]) ^ gmult(0x09, col[2]) ^ gmult(0x0e, col[3])
    return state

def add_round_key(state, key_int):
    key_state = num_to_state(key_int)
    for r in range(4):
        for c in range(4):
            state[r][c] ^= key_state[r][c]
    return state

def decrypt_block(ciphertext_int, keys):
    # 1. Pre-Round: AddRoundKey(Key10)
    state = num_to_state(ciphertext_int)
    state = add_round_key(state, keys[10])

    # 2. Rounds 1 to 9
    for r in range(9, 0, -1): # Keys 9 down to 1
        state = inv_shift_rows(state)
        state = inv_sub_bytes(state)
        state = add_round_key(state, keys[r])
        state = inv_mix_columns(state)

    # 3. Round 10 (Last Round)
    state = inv_shift_rows(state)
    state = inv_sub_bytes(state)
    state = add_round_key(state, keys[0])

    return state_to_num(state)

def int_to_text(val):
    try:
        # Convert to hex string then bytes
        hex_str = f"{val:032x}"
        return bytes.fromhex(hex_str).decode('utf-8').replace('\x00', '')
    except:
        return "[Non-printable or Error]"

